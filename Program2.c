// Accept two number form user and perform addition
#include<stdio.h>

int main()
{
    int i,j,k;

    printf("Enter first number:\n");
    scanf("%d",&i);

    printf("Enter Second number:\n");
    scanf("%d",&j);

    k = i + j;

    printf("Addition of number: %d\n",k);

    return 0;


}